#import "NSMutableString+AZNetworkingMethods.h"
#import "NSObject+AZNetworkingMethods.h"

@implementation NSMutableString (AZNetworkingMethods)

- (void)appendURLRequest:(NSURLRequest *)request
{
    [self appendFormat:@"\n\nHTTP URL:\n\t%@", request.URL];
    [self appendFormat:@"\n\nHTTP Header:\n%@", request.allHTTPHeaderFields ? request.allHTTPHeaderFields : @"\t\t\t\t\tN/A"];
    [self appendFormat:@"\n\nHTTP Body:\n\t%@", [[[NSString alloc] initWithData:request.HTTPBody encoding:NSUTF8StringEncoding] AZ_defaultValue:@"\t\t\t\tN/A"]];
}

@end
